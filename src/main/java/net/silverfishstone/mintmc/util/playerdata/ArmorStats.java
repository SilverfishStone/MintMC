package net.silverfishstone.mintmc.util.playerdata;

public class ArmorStats {
    private int damage;
    private int fire;
    private int blast;
    private int combat;

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public int getFire() {
        return fire;
    }

    public void setFire(int fire) {
        this.fire = fire;
    }

    public int getBlast() {
        return blast;
    }

    public void setBlast(int blast) {
        this.blast = blast;
    }

    public int getCombat() {
        return combat;
    }

    public void setCombat(int combat) {
        this.combat = combat;
    }
}